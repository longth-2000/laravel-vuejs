     * @param  \App\Models\User  $user
     * @param  \App\Models\Post  $post
     * @return \Illuminate\Auth\Access\Response|bool
     */
    public function update(User $user, Post $post)
    {
        $permissions = DB::table('user-permission')
        ->join('users', 'user-permission.userID', '=', 'users.userID')
        ->join('permissions', 'user-permission.permissionID', '=', 'permissions.permissionID')
        ->select('permissions.slug')
        ->where('users.userID', '=', $user->userID)
        ->get();
       
        return $permissions->firstWhere('slug', 'update-post') !== null; 
    }

